// Object.key is a object's build in method for getting keys of a object

const items = {
    rice: "5kg",
    oil: "10kg",
    egg: "1 dozen"
}
console.log(Object.keys(items)); // Output:- [ 'rice', 'oil', 'egg' ]


// getting values of a object
console.log(Object.values(items)); // Output:- [ '5kg', '10kg', '1 dozen' ]